package Fundamentos;

public class inferencia {
	
public static void main(String[] args) {
        

        double a = 15.5; //declaração e inicialização
        System.out.println(a);
        
        var b = 15.5;
        System.out.println(b);
        
        var c = "Texto";
        System.out.println(c);
        
        c = "Outro Texto";
        System.out.println(c);

	  double d;   // variavel foi declarada
	  d = 123.23; // variavel foi inicializada
	  System.out.println(d);   // variavel foi imprimida


}

}
